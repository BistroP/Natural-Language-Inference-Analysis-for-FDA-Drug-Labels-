import time
import requests

def fetch_pubmed_abstracts(drug_name: str,
                            start_year: int,
                            end_year: int,
                            abstracts_per_year: int = 25) -> pd.DataFrame:
    """
    Fetch PubMed abstracts mentioning drug_name, grouped by year.
    Uses NCBI E-utilities (no API key required, but rate-limited to 3 req/sec).
    Returns DataFrame with columns: drug, year, abstract, scored features.
    """
    base_url = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils"
    rows = []

    for year in range(start_year, end_year + 1):
        # Step 1: search for PMIDs
        search_params = {
            "db": "pubmed",
            "term": f"{drug_name}[tiab] AND {year}[pdat]",
            "retmax": abstracts_per_year,
            "retmode": "json",
            "sort": "relevance",
        }
        try:
            r = requests.get(f"{base_url}/esearch.fcgi", params=search_params)
            r.raise_for_status()
            pmids = r.json()["esearchresult"]["idlist"]
        except Exception as e:
            print(f"  [{drug_name}] search failed for {year}: {e}")
            time.sleep(0.5)
            continue

        if not pmids:
            print(f"  [{drug_name}] no results for {year}")
            continue

        time.sleep(0.34)  # stay under 3 req/sec without API key

        # Step 2: fetch abstracts for those PMIDs
        fetch_params = {
            "db": "pubmed",
            "id": ",".join(pmids),
            "rettype": "abstract",
            "retmode": "xml",
        }
        try:
            r = requests.get(f"{base_url}/efetch.fcgi", params=fetch_params)
            r.raise_for_status()
            soup = BeautifulSoup(r.content, "lxml-xml")
        except Exception as e:
            print(f"  [{drug_name}] fetch failed for {year}: {e}")
            time.sleep(0.5)
            continue

        time.sleep(0.34)

        # Step 3: extract and score each abstract
        abstracts = soup.find_all("AbstractText")
        year_texts = [a.get_text(separator=" ", strip=True)
                      for a in abstracts if len(a.get_text()) > 100]

        if not year_texts:
            print(f"  [{drug_name}] no usable abstracts for {year}")
            continue

        for text in year_texts:
            try:
                scores = score_severity(text)
                rows.append({
                    "drug":  drug_name,
                    "year":  year,
                    "abstract": text,
                    **scores,
                })
            except Exception as e:
                print(f"  [{drug_name}] scoring failed for {year}: {e}")

        print(f"  [{drug_name}] {year}: {len(year_texts)} abstracts scored")

    df = pd.DataFrame(rows)
    return df


def build_pubmed_yearly(drug_name: str,
                         start_year: int,
                         end_year: int) -> pd.DataFrame:
    """
    Aggregate per-abstract scores to yearly means for comparison
    against label trajectories.
    """
    df = fetch_pubmed_abstracts(drug_name, start_year, end_year)
    if df.empty:
        return df

    score_cols = [c for c in df.columns if c.endswith("_severity_score")]
    yearly = (df.groupby("year")[score_cols]
                .mean()
                .reset_index())
    yearly["drug"] = drug_name
    return yearly

def plot_pubmed_vs_label(label_df: pd.DataFrame,
                          pubmed_df: pd.DataFrame,
                          drug_name: str):
    """
    Overlay yearly mean PubMed NLI scores against label NLI scores.
    Compares trajectory shape/direction — not absolute values.
    """
    dims = ["causal", "outcome", "population", "clinical"]
    colors = {"label": "crimson", "pubmed": "steelblue"}

    fig, axes = plt.subplots(len(dims), 1,
                              figsize=(13, 3.5 * len(dims)),
                              sharex=False)
    fig.suptitle(
        f"{drug_name.title()} — Label vs. PubMed NLI Severity Trajectories\n"
        f"(trajectory shape comparable; absolute scale differs by corpus)",
        fontsize=13, y=1.01
    )

    # Convert label dates to year for alignment
    label_df = label_df.copy()
    label_df["year"] = label_df["date"].dt.year
    label_yearly = label_df.groupby("year")[
        [f"{d}_severity_score" for d in dims]
    ].mean().reset_index()

    for ax, dim in zip(axes, dims):
        col = f"{dim}_severity_score"

        # Label trajectory
        ax.plot(label_yearly["year"], label_yearly[col],
                marker="o", color=colors["label"],
                linewidth=2, label="FDA Label")

        # PubMed trajectory — secondary y-axis to handle scale difference
        ax2 = ax.twinx()
        if col in pubmed_df.columns:
            ax2.plot(pubmed_df["year"], pubmed_df[col],
                     marker="s", color=colors["pubmed"],
                     linewidth=2, linestyle="--", label="PubMed")
            ax2.set_ylabel("PubMed score", fontsize=8, color=colors["pubmed"])
            ax2.tick_params(axis="y", labelcolor=colors["pubmed"])

        ax.set_ylabel(f"{dim} severity\n(label)", fontsize=8,
                      color=colors["label"])
        ax.tick_params(axis="y", labelcolor=colors["label"])
        ax.set_title(dim.capitalize(), fontsize=9)
        ax.grid(True, alpha=0.3)
        ax.axhline(0, color="black", linewidth=0.6, linestyle="--", alpha=0.3)

        # Combined legend
        lines1, labels1 = ax.get_legend_handles_labels()
        lines2, labels2 = ax2.get_legend_handles_labels()
        ax.legend(lines1 + lines2, labels1 + labels2, fontsize=8)

    axes[-1].set_xlabel("Year")
    plt.tight_layout()
    fname = f"{drug_name.lower()}_pubmed_vs_label.png"
    plt.savefig(fname, dpi=150, bbox_inches="tight")
    print(f"Saved → {fname}")
    plt.show()

