# Zero-shot NLI severity scorer
# We score each warning block against two hypotheses and take the
# difference as a directional "regulatory severity" signal.
_nli = pipeline(
    "zero-shot-classification",
    model="facebook/bart-large-mnli",
    device=0,
)
CAUSAL_SEVERE_HYPOTHESIS = "This drug label makes direct, unhedged assertions that the drug causes specific harmful outcomes."
CAUSAL_MILD_HYPOTHESIS   = "This drug label describes potential risks tentatively, using uncertain or qualified language."
CAUSAL_HYPOTHESIS = [CAUSAL_SEVERE_HYPOTHESIS, CAUSAL_MILD_HYPOTHESIS]
OUTCOME_SEVERE_HYPOTHESIS = "This drug label describes risks that are life-threatening, fatal, or cause permanent harm."
OUTCOME_MILD_HYPOTHESIS = "This drug label describes risks that are mild, temporary, or reversible."
OUTCOME_HYPOTHESIS = [OUTCOME_SEVERE_HYPOTHESIS, OUTCOME_MILD_HYPOTHESIS]
POPULATION_SEVERE_HYPOTHESIS =  "This drug label describes risks that apply broadly to most or all patients taking this drug."
POPULATION_MILD_HYPOTHESIS = "This drug label describes risks that are limited to specific or narrow patient subgroups."
POPULATION_HYPOTHESIS = [POPULATION_SEVERE_HYPOTHESIS, POPULATION_MILD_HYPOTHESIS]
CLINICAL_SEVERE_HYPOTHESIS = "This drug label requires specific urgent clinical actions such as monitoring, discontinuation, or immediate intervention."
CLINICAL_MILD_HYPOTHESIS =  "This drug label describes risks without mandating specific or urgent clinical responses."
CLINICAL_HYPOTHESIS = [CLINICAL_SEVERE_HYPOTHESIS, CLINICAL_MILD_HYPOTHESIS]
HYPOTHESIS = [CAUSAL_HYPOTHESIS, OUTCOME_HYPOTHESIS, POPULATION_HYPOTHESIS, CLINICAL_HYPOTHESIS]
def score_severity(text: str) -> dict:
    truncated = " ".join(text.split()[:400])
    types = ["causal", "outcome", "population", "clinical"]
    output = {}
    for i, current_hypothesis in enumerate(HYPOTHESIS):
        result = _nli(
            truncated,
            candidate_labels=current_hypothesis,
            hypothesis_template="{}",
        )
        label_score = dict(zip(result["labels"], result["scores"]))
        p_severe = label_score.get(current_hypothesis[0], 0.0)
        p_mild   = label_score.get(current_hypothesis[1], 0.0)
        t = types[i]
        output[f"{t}_severity_score"] = round(p_severe - p_mild, 4)
        output[f"{t}_p_severe"]       = round(p_severe, 4)
        output[f"{t}_p_mild"]         = round(p_mild, 4)
    return output

