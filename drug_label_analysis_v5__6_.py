"""## DailyMed helpers"""

def search_dailymed(drug_name: str, pagesize: int = 5) -> list[dict]:
    """Search DailyMed for a drug. Returns list of results with 'setid' key."""
    r = requests.get(
        "https://dailymed.nlm.nih.gov/dailymed/services/v2/spls.json",
        params={"drug_name": drug_name, "pagesize": pagesize},
    )
    r.raise_for_status()
    return r.json()["data"]


def get_set_id(drug_name: str) -> str:
    """Convenience: return the top-ranked set_id for a drug name."""
    results = search_dailymed(drug_name)
    if not results:
        raise ValueError(f"No DailyMed results for '{drug_name}'")
    return results[0]["setid"]


def get_archive_versions(set_id: str) -> list[dict]:
    """Scrape all versioned archive entries for a set_id.
    Returns list of dicts with keys: archive_id, version, date, url.
    """
    r = requests.get(f"https://dailymed.nlm.nih.gov/dailymed/drugInfo.cfm?setid={set_id}")
    soup = BeautifulSoup(r.text, "html.parser")
    links = soup.find_all("a", href=lambda h: h and "getArchivalFile.cfm" in h)
    versions = []
    for link in links:
        href = link["href"]
        archive_id = href.split("archive_id=")[-1].split("&")[0]
        row   = link.find_parent("tr")
        cells = row.find_all("td") if row else []
        versions.append({
            "archive_id": archive_id,
            "version":    cells[1].get_text(strip=True) if len(cells) > 1 else None,
            "date":       cells[0].get_text(strip=True) if len(cells) > 0 else None,
            "url":        f"https://dailymed.nlm.nih.gov/dailymed/getArchivalFile.cfm?archive_id={archive_id}",
        })
    return versions


def download_label_xml(download_url: str) -> BeautifulSoup:
    """Download a versioned ZIP from DailyMed and return its SPL XML as a BeautifulSoup tree."""
    r = requests.get(download_url)
    r.raise_for_status()
    z = zipfile.ZipFile(io.BytesIO(r.content))
    xml_file = next(f for f in z.namelist() if f.endswith(".xml"))
    return BeautifulSoup(z.read(xml_file), "lxml-xml")


def extract_sections(soup: BeautifulSoup) -> dict[str, str]:
    """Return {section_title: text} for every titled section in an SPL XML tree."""
    sections = {}
    for section in soup.find_all("section"):
        title = section.find("title")
        text  = section.find("text")
        if title and text:
            sections[title.get_text(strip=True)] = text.get_text(separator=" ", strip=True)
    return sections


def extract_warnings_text(sections: dict[str, str]) -> str:
    """Join all warning/precaution/boxed sections into one string."""
    keywords = {"WARN", "PRECAUTION", "BOXED"}
    return " ".join(
        text for name, text in sections.items()
        if any(kw in name.upper() for kw in keywords)
    )

