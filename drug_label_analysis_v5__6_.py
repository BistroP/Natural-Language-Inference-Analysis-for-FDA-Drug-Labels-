# Expanding the set here to be in
HEDGING_WORDS = {
    # Modal auxiliaries
    "may", "might", "could", "would",
    # Verbs of hedging
    "appear", "appears", "seem", "seems", "suggest", "suggests",
    "indicate", "indicates", "believe", "believed",
    # Epistemic adjectives/adverbs
    "possibly", "potentially", "likely", "unlikely", "probable",
    "possible", "potential", "apparent", "apparently",
    "uncertain", "unclear", "suspected", "putative",
    # Conjunctive hedges
    "whether",
}
EPISTEMIC_CERTAINTY_WORDS = {
    "known", "proven", "confirmed", "established",
    "demonstrated", "shown", "documented", "verified",
}

DEONTIC_CERTAINTY_WORDS = {
    "must", "required", "required", "shall",
    "necessary", "mandatory", "contraindicated",
}
CAUSAL_PHRASES = [
    "associated with", "linked to", "leads to", "results in",
    "increased the risk", "increases the risk", "caused by",
    "due to", "attributed to",
]
NEGATED_CAUSAL = [
    "did not show", "no evidence", "failed to demonstrate",
    "not established", "no increase",
]


def extract_features(text: str) -> dict:
    """Extract interpretable linguistic features + NLI severity score."""
    doc        = nlp(text.lower())
    tokens     = [t.text for t in doc]
    text_lower = text.lower()
    word_count = max(len([t for t in doc if not t.is_punct]), 1)

    # spaCy passive: covers both 'auxpass' (older models) and 'aux:pass'
    passive_count = sum(
        1 for t in doc
        if t.dep_ in {"auxpass", "aux:pass"}
    )
    causal_hits     = sum(text_lower.count(p) for p in CAUSAL_PHRASES)
    neg_causal_hits = sum(text_lower.count(p) for p in NEGATED_CAUSAL)

    features = {
        "passive_rate":           passive_count / word_count,
        "hedging_rate":             sum(1 for t in tokens if t in HEDGING_WORDS) / word_count,
        "epistemic_certainty_rate": sum(1 for t in tokens if t in EPISTEMIC_CERTAINTY_WORDS) / word_count,
        "deontic_certainty_rate":   sum(1 for t in tokens if t in DEONTIC_CERTAINTY_WORDS) / word_count,
        "causal_rate":            causal_hits     / word_count,
        "negated_causal_rate":    neg_causal_hits / word_count,
        "word_count":             word_count,
    }

    features.update(score_severity(text))
    return features

"""## Full pipeline"""

def build_drug_dataframe(drug_name: str) -> pd.DataFrame:
    """
    End-to-end: search DailyMed → scrape archive → download each version →
    extract features → return a tidy DataFrame sorted by date.

    Key columns:
      severity_score  P(severe) - P(mild) — the main directional sentiment signal
      p_severe        raw probability of severe/life-threatening framing
      p_mild          raw probability of mild/permissive framing
      hedging_rate, certainty_rate, passive_rate, causal_rate, negated_causal_rate
    """
    print(f"[{drug_name}] looking up set_id …")
    set_id   = get_set_id(drug_name)
    versions = get_archive_versions(set_id)
    print(f"[{drug_name}] found {len(versions)} archived versions (set_id={set_id})")

    rows = []
    for v in versions:
        try:
            soup     = download_label_xml(v["url"])
            sections = extract_sections(soup)
            warnings = extract_warnings_text(sections)
            if not warnings:
                continue
            features = extract_features(warnings)
            rows.append({
                "drug":          drug_name,
                "version":       v["version"],
                "date":          v["date"],
                "warnings_text": warnings,
                **features,
            })
        except Exception as e:
            print(f"  version {v['version']} failed: {e}")

    df = pd.DataFrame(rows)
    df["date"]    = pd.to_datetime(df["date"], format="mixed")
    df["version"] = df["version"].str.strip().str.split("").str[0]
    df = df.sort_values("date").reset_index(drop=True)
    return df

